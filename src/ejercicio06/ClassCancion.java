

package ejercicio06;


public class ClassCancion {
    
    private String titulo;
    private String autor;

    
    // CONSTRUCTORES 
    
    public ClassCancion() {
    }

    public ClassCancion(String titulo, String autor) {
        this.titulo = titulo;
        this.autor = autor;
    }
    
    
    // GETTER Y SETTER

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }
    
    
    
   
    
    
    
    
    
    
}
